                    Teléfono: (608) 584 2347<br>
                    Correo electrónico: <a href="mailto:guaviare@sena.edu.co">guaviare@sena.edu.co</a>
                </p>

                <h6 class="mt-4"><strong>4. Autorización para el tratamiento de datos personales</strong></h6>
                <p>
                    Mediante la aceptación expresa de esta política, el usuario autoriza de manera 
                    <strong>libre, previa, voluntaria y debidamente informada</strong> al 
                    <strong>SENA Regional Guaviare – CDATTG</strong> para recolectar, almacenar, usar, circular, procesar 
                    y suprimir sus datos personales conforme a las finalidades descritas en este documento.
                </p>
                <p>El usuario reconoce que ha sido informado de:</p>
                <ul>
                    <li>Los fines específicos del tratamiento de sus datos personales.</li>
                    <li>El carácter facultativo de responder preguntas sobre datos sensibles o sobre niñas, niños y adolescentes.</li>
                    <li>Sus derechos como titular de los datos y los mecanismos disponibles para ejercerlos.</li>
                </ul>
                <p>
                    La autorización se entenderá otorgada al momento de aceptar los términos y condiciones de uso o 
                    registrar sus datos personales en los sistemas del SENA Regional Guaviare.
                </p>

                <h6 class="mt-4"><strong>5. Ejercicio de derechos y consultas</strong></h6>
                <p>
                    Los titulares podrán presentar <strong>consultas, reclamos o solicitudes</strong> relacionadas con la protección 
                    y tratamiento de sus datos personales ante el <strong>SENA Regional Guaviare – CDATTG</strong>, 
                    a través de los siguientes medios:
                </p>
                <ul>
                    <li>Correo electrónico: <a href="mailto:guaviare@sena.edu.co">guaviare@sena.edu.co</a></li>
                    <li>Presencialmente: Oficina Regional – Carrera 23 No. 12-31, San José del Guaviare, Colombia</li>
                    <li>Línea telefónica: (608) 584 2347</li>
                </ul>
                <p>
                    El SENA atenderá las solicitudes en los plazos establecidos por la Ley 1581 de 2012 y sus decretos reglamentarios.
                </p>

                <h6 class="mt-4"><strong>6. Vigencia de la política</strong></h6>
                <p>
                    La presente política rige a partir de su publicación y tendrá vigencia indefinida, mientras el 
                    <strong>SENA Regional Guaviare – CDATTG</strong> continúe ejerciendo actividades que involucren 
                    el tratamiento de datos personales dentro de sus sistemas o procesos institucionales.
                </p>

                <h6 class="mt-4"><strong>7. Aceptación</strong></h6>
                <p>
                    Al marcar la casilla de aceptación o continuar con el registro de información, el usuario declara 
                    que ha leído, comprendido y acepta expresamente la presente 
                    <strong>Política de Tratamiento de Datos Personales</strong>, otorgando su autorización para el uso 
                    de su información conforme a lo aquí establecido.
                </p>
