            <div class="modal-body" style="text-align: justify; line-height: 1.6;">
                <p>
                    En cumplimiento de lo dispuesto por la <strong>Ley 1581 de 2012</strong>, el 
                    <strong>Decreto Reglamentario 1377 de 2013</strong> y demás normas complementarias sobre 
                    la protección de datos personales, el <strong>Servicio Nacional de Aprendizaje (SENA) – Regional Guaviare</strong>, 
                    a través del <strong>Centro para el Desarrollo Agroindustrial, Turístico y Tecnológico del Guaviare (CDATTG)</strong>, 
                    informa a los usuarios que los datos personales recolectados serán tratados conforme a los principios de 
                    <em>legalidad, finalidad, libertad, veracidad, transparencia, acceso y circulación restringida, seguridad y confidencialidad</em>.
                </p>

                <h6 class="mt-4"><strong>1. Finalidad del tratamiento</strong></h6>
                <p>Los datos personales suministrados por los usuarios serán utilizados con los siguientes propósitos:</p>
                <ul>
                    <li>Registrar y administrar la información de aprendices, instructores y personal administrativo.</li>
                    <li>Gestionar procesos de asistencia, control y seguimiento académico o institucional.</li>
                    <li>Generar reportes y estadísticas internas para la mejora de los procesos formativos y administrativos.</li>
                    <li>Cumplir con obligaciones legales, contractuales o institucionales propias del SENA.</li>
                    <li>Enviar notificaciones, comunicados y recordatorios institucionales relacionados con el proceso formativo o la gestión administrativa.</li>
                    <li>Garantizar la seguridad, trazabilidad y conservación de la información registrada en los sistemas institucionales.</li>
                </ul>

                <h6 class="mt-4"><strong>2. Derechos del titular de los datos</strong></h6>
                <p>De acuerdo con lo establecido en la Ley 1581 de 2012, los titulares de la información tienen los siguientes derechos:</p>
                <ul>
                    <li>Conocer, actualizar y rectificar sus datos personales frente al SENA o ante los encargados del tratamiento.</li>
                    <li>Solicitar la eliminación de sus datos personales cuando considere que no se están utilizando conforme a los fines autorizados.</li>
                    <li>Revocar la autorización otorgada para el tratamiento de sus datos personales.</li>
                    <li>Acceder gratuitamente a los datos personales que hayan sido objeto de tratamiento.</li>
                    <li>Ser informado sobre el uso que se ha dado a sus datos personales.</li>
                </ul>

                <h6 class="mt-4"><strong>3. Responsable del tratamiento</strong></h6>
                <p>
                    <strong>Servicio Nacional de Aprendizaje – SENA</strong><br>
                    <strong>Regional Guaviare</strong><br>
                    <strong>Centro para el Desarrollo Agroindustrial, Turístico y Tecnológico del Guaviare (CDATTG)</strong><br>
